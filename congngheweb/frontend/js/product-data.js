(function () {
  const fallbackProducts = Array.isArray(window.BAKERY_PRODUCTS) ? window.BAKERY_PRODUCTS.slice() : [];
  if (typeof window === 'undefined' || typeof window.fetch !== 'function') {
    window.BAKERY_PRODUCTS_READY = Promise.resolve(fallbackProducts);
    return;
  }

  const fetchProducts = window.bakeryApiFetch || window.fetch.bind(window);
  window.BAKERY_PRODUCTS_READY = (async () => {
    try {
      const response = await fetchProducts('/api/products');
      if (!response || !response.ok) {
        throw new Error('Failed to load products');
      }

      const payload = await response.json();
      const products = Array.isArray(payload?.products) ? payload.products : fallbackProducts;
      window.BAKERY_PRODUCTS = products;
      window.dispatchEvent(new Event('bakery-products-loaded'));
      return products;
    } catch {
      window.BAKERY_PRODUCTS = fallbackProducts;
      return fallbackProducts;
    }
  })();
})();
